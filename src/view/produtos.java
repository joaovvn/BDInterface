    private void CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarActionPerformed
        txtPreco.setText(txtPreco.getText().replaceAll(",", "."));
        int cadastro = 0;
        Produtos p = new Produtos();
        ProdutoDAO dao = new ProdutoDAO();
        for(Produtos prod : dao.read()) {
            if(Integer.parseInt(txtCodigo.getText()) == prod.getCodigo()){
                cadastro++;
            }
        }
        if(cadastro > 0) {
            JOptionPane.showMessageDialog(null, "Um produto com esse código já existe, tente novamente com outro código!");
        }
        else{
            p.setCodigo(Integer.parseInt(txtCodigo.getText()));
            p.setNome(txtNome.getText());
            p.setFornecedor(txtFornecedor.getText());
            p.setPreco(Double.parseDouble(txtPreco.getText()));
            p.setQuantidade(Integer.parseInt(txtQtd.getText()));
            p.setTipo(txtTipo.getText());
            dao.create(p);
            readJTable();
        }
        txtNome.setText("");
        txtFornecedor.setText("");
        txtPreco.setText("");
        txtCodigo.setText("");
        txtTipo.setText("");
        txtQtd.setText("");
        txtCodigo.setEditable(true);

    }//GEN-LAST:event_CadastrarActionPerformed

    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
        if(tabProdutos.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(null, "Nenhum produto foi selecionado!");
        }
        else{
        txtPreco.setText(txtPreco.getText().replaceAll(",", "."));
        
            Produtos p = new Produtos();
            ProdutoDAO dao = new ProdutoDAO();
            p.setCodigo(Integer.parseInt(txtCodigo.getText()));
            p.setNome(txtNome.getText());
            dao.delete(p);
            readJTable();
        txtNome.setText("");
        txtFornecedor.setText("");
        txtPreco.setText("");
        txtCodigo.setText("");
        txtTipo.setText("");
        txtQtd.setText("");
        }
        txtCodigo.setEditable(true);
    }//GEN-LAST:event_ExcluirActionPerformed

    private void tabProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabProdutosMouseClicked
        if(tabProdutos.getSelectedRow() != -1){
            txtNome.setText(tabProdutos.getValueAt(tabProdutos.getSelectedRow(), 0).toString());
            txtFornecedor.setText(tabProdutos.getValueAt(tabProdutos.getSelectedRow(), 1).toString());
            txtPreco.setText(tabProdutos.getValueAt(tabProdutos.getSelectedRow(), 2).toString());
            txtCodigo.setText(tabProdutos.getValueAt(tabProdutos.getSelectedRow(), 3).toString());
            txtTipo.setText(tabProdutos.getValueAt(tabProdutos.getSelectedRow(), 4).toString());
            txtQtd.setText(tabProdutos.getValueAt(tabProdutos.getSelectedRow(), 5).toString());
            txtCodigo.setEditable(false);
        }
    }//GEN-LAST:event_tabProdutosMouseClicked

