    public void readJTable() {
    DefaultTableModel modelo = (DefaultTableModel) tabProdutos.getModel();
    modelo.setNumRows(0);
    ProdutoDAO dao = new ProdutoDAO();
    for(Produtos p : dao.read()) {
        modelo.addRow(new Object[]{
            p.getNome(),
            p.getFornecedor(),
            p.getPreco(),
            p.getCodigo(),
            p.getTipo(),
            p.getQuantidade()
        });
    }
}

